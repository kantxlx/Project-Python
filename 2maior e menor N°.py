def maximo(a,b,c):
    return max(a,b,c)
def menor(a,b,c):
    return min(a,b,c)

a = int(input("digite um numero: "))
b = int(input("digite outro numero: "))
c = int(input("digite o ultimo numero: "))

maior = max(a,b,c)
meno = min(a,b,c)

print("o maior numero e: ",maior) 
print("o menor numero e: ",meno)